    private void printTargetMap(int value) {
        switch(value) {
            //Empty pixel
            case 0 -> 
                System.out.print("  ");
            //Food pixel
            case 1 -> 
                System.out.print("· ");
            //Poweup pixel
            case 2 -> 
                System.out.print("@ ");
            //Fruit pixel
            case 3 -> 
                System.out.print("F ");
            //Wall pixel
            case 4 -> 
                System.out.print("■ ");
            //Ghost zone pixels
            case 5 -> System.out.print("  ");
            //Void pixels
            case 6 -> System.out.print("  ");
            
        }
    }
    
    public void printMap() {
        for(int i=0; i<mapHeight; i++) {
            for(int j=0; j<mapWidth; j++){
                printTargetMap(mapData[i][j]);
            }
            System.out.print("\n");
        }
    }
    
   public int countAmountOfLegalPlaces() {
       int count = 0;
       for(int i=0; i<mapHeight; i++) {
           for(int j=0; j<mapWidth; j++) {
               if(mapData[i][j] != 4 && mapData[i][j] != 6) {
                   count++;
               }
           }
       }
       
       return count;
   }
    
